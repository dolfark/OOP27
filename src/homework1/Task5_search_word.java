public class Task5_search_word {
	public static void main(String[] args) throws IOException {
		BufferedReader read = new BufferedReader(new FileReader("c:/tmp/test.txt"));
		Scanner inFile = new Scanner(read);
		Scanner in = new Scanner(System.in);
		System.out.println("Enter word");
		String wordSearch = in.nextLine();
		int countLine = 0;
		int countNumber = 0;
		int countWord = 0;

		while (inFile.hasNextLine()) {
			countLine += 1;
			String str = inFile.nextLine();
			String[] strSplit = str.split(" ");
			countNumber = 0;
			for (int i = 0; i < strSplit.length; i++) {
				countNumber += 1;
				if (wordSearch.equals(strSplit[i].toString())) {
					countWord += 1;
					System.out.println("word found in " + countLine + " line " + countNumber + " number");
				}

			}

		}
		System.out.println("In total " + countWord + " times");

	}

}
